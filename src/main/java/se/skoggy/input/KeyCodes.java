package se.skoggy.input;

public abstract class KeyCodes {

	public abstract int left();
	public abstract int right();
	public abstract int up();
	public abstract int down();

	public abstract int axisLeftX();
	public abstract int axisLeftY();
	public abstract int axisRightX();
	public abstract int axisRightY();

	public abstract int start();
	public abstract int back();

	public abstract int action1();
	public abstract int action2();
	public abstract int action3();
	public abstract int action4();
	public abstract int shoulderLeft();
	public abstract int shoulderRight();

	public abstract int triggerLeft();
	public abstract int triggerRight();
}
